using System;

class counting
{
    public static void Main()
    {
        int i;
        for (i = 0; i <= 50; i += 5)
        {
            Console.WriteLine("i = {0}", i);
        }

        Console.WriteLine("\n");

        for(i=100; i > 0; i--)
        {
            Console.WriteLine("i = {0}", i);
        }
    }

}

